package Day05_Operators;

public class ShortHandOperators {
    public static void main(String[] args) {
        int x = 5, y = 10;

        x+=y; // x=x+y;
        System.out.println(x);
        x-=y; // x=x-y;
        System.out.println(x);
        x*=y; // x=x*y;
        System.out.println(x);
        x/=y; // x=x/y;
        System.out.println(x);
        x%=y; // x=x%y;

        System.out.println(x);
    }
}
